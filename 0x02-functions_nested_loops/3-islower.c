/**
  * _islower - perceives whether a character is lower case
  *
  * Return: 1 if lower, 0 if other
  */
int _islower(int)
{
int c;
if (c >= 'A' && c <= 'Z')
{
	return (1);
}
else
{
	return (0);
}
}
